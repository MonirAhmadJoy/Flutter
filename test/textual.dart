class textual {
  String situation(int index) {
    if (index == 1)
      return "Choking";
    else if (index == 2)
      return "Acid Attack";
    else if (index == 3)
      return "Burns";
    else if (index == 4)
      return "Head Injury";
    else if (index == 5)
      return "Earthquake";
    else if (index == 6)
      return "Stroke";
    else
      return "Drowning";
  }
}
